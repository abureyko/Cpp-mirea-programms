#include <iostream>
using namespace std;

int main(){
    int n, m;
    cin >> n >> m;

    int ans_x = 0;
    int ans_y = 0;

    // Считаем по X
    for (int x = 0; x < n; x++)  
        ans_x += n - x;  

    // Считаем по Y  
    for (int y = 0; y < m; y++)  
        ans_y += m - y;  

    cout << ans_x*ans_y << endl;  
}

// Алгоритм вычисляет количество всех возможных прямоугольников, которые можно вырезать из листа N×M клеток
// 
// Для подсчета прямоугольников используем комбинаторный подход:
// - Чтобы задать прямоугольник, нужно выбрать две различные вертикальные границы (левую и правую)
//   и две различные горизонтальные границы (верхнюю и нижнюю)
//
// Количество способов выбрать горизонтальные границы:
//   Левая граница может быть на любой из N+1 позиций (между клетками и по краям)
//   Правая граница должна быть правее левой
//   Это эквивалентно сумме 1 + 2 + ... + N = N*(N+1)/2
//
// Аналогично для вертикальных границ: M*(M+1)/2
//
// Итоговая формула: количество_прямоугольников = [N*(N+1)/2] * [M*(M+1)/2]
//
// В программе это вычисляется через циклы, суммируя (N-i) для i от 0 до N-1, что дает тот же результат N*(N+1)/2
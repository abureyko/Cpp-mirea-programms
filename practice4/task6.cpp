#include <iostream>
#include <iomanip>
using namespace std;

int main() {
    // Матрица A: количество проданных товаров (3 продавца × 4 товара)
    double A[3][4] = {
        {2, 5, 0, 3},  // Продавец 1
        {1, 3, 4, 2},  // Продавец 2
        {0, 6, 2, 1}   // Продавец 3
    };
    
    // Матрица B: цены и комиссионные (4 товара × 2)
    double B[4][2] = {
        {2.80, 0.40},  // Товар 1
        {3.00, 1.00},  // Товар 2  
        {2.00, 1.50},  // Товар 3
        {2.50, 0.80}   // Товар 4
    };

    // Матрица C: результат умножения (3 продавца × 2)
    double C[3][2] = {0};
    
    // Умножение матриц: C = A × B
    for (int i = 0; i < 3; i++) {
        for (int j = 0; j < 2; j++) {
            for (int k = 0; k < 4; k++) {
                C[i][j] += A[i][k] * B[k][j];
            }
        }
    }
    
    cout << "   Цена  Комиссонные" << endl;
    for (int i = 0; i < 3; i++) {
        cout << i+1 << '.';
        for (int j = 0; j < 2; j++) {
            cout << " " << C[i][j] << " "; 
        }
        cout << endl;
    }
    double sum;

    for (int i = 0; i < 3; i++){
        for (int j = 0; j < 2; j++)
        sum += C[i][j];
    }

    cout << "\nВопрос 1: " << C[0][0] << " " << C[1][0] << endl;
    cout << "Вопрос 2: "<< C[1][1] << endl;
    cout << "Вопрос 3: "<< C[0][0]+C[1][0]+C[2][0] << endl;
    cout << "Вопрос 4: " << C[0][1]+C[1][1]+C[2][1]<< endl;
    cout << "Вопрос 5: " << sum << endl;

    return 0;
}
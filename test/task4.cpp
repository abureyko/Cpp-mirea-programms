#include <iostream>
using namespace std;

int section(int n)
{
    if (n <= 36){
        return (n - 1) / 4;
    }
    else{
        return 8 - (n - 37) / 2;
    }
}



int main()
{
    int arr[9] = {0};
    int N;
    cin >> N;
    int input;
    for (int i = 0; i < N; i++){
        cin >> input;
        arr[section(input)]++;
    }
    int ans = 0;
    int curr = 0;
    for (int i : arr){
        if (i==6){
            curr++;
            ans = max(ans, curr);
        }
        else{
            curr = 0;
        }
    }

    cout << ans << endl;

    return 0;
} 

// Функция section определяет номер купе (от 0 до 8) для каждого места в плацкартном вагоне. 
// Для мест 1-36 используется формула (k-1)/4, а для боковых мест 37-54 - формула 8-(k-37)/2. 
// Программа считывает количество пассажиров и распределяет их по купе, увеличивая счётчики в массиве. 
// Затем находится самая длинная последовательность купе, где в каждом ровно 6 пассажиров. 
// Результатом является длина этой максимальной последовательности.





